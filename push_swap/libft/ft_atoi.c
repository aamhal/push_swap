#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	size_t	result;
	int		signe;

	i = 0;
	result = 0;
	signe = 1;
	while (str[i] && ((str[i] >= 9 && str[i] <= 13) || str[i] == 32))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			signe *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - 48);
		i++;
	}
	if (result > 2147483648 && signe == -1)
		return (-1);
	else if (result > 2147483647 && signe == 1)
		return (-1);
	return (result * signe);
}
